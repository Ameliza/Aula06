package poo;

public class Aritmetica {

    /**
     * Esse método faz a soma de dois inteiros
     * @param a
     * @param b
     * @return
     */
    public int soma(int a, int b){
        return a+b;
    }

    /**
     * Esse método faz a subtração de dois inteiros
     * @param a
     * @param b
     * @return
     */
    public int sub(int a, int b){
        return a-b;
    }

    /**
     * Esse método faz multiplicação de dois inteiros
     * @param a
     * @param b
     * @return
     */
    public int mult(int a, int b){
        return a*b;
    }

    /**
     * Esse método faz a divisão de dois floats
     * @param a
     * @param b
     * @return
     */
    public float div(int a, int b){
        return (float)a/b;
    }

}
